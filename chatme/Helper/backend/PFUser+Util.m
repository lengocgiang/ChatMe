#import "PFUser+Util.h"
@import Parse;

@implementation PFUser (Util)
//-------------------------------------------------------------------------------------------------------------------------------------------------
- (NSString *)fullname
//-------------------------------------------------------------------------------------------------------------------------------------------------
{
    return self[PF_USER_FULLNAME];
}

//-------------------------------------------------------------------------------------------------------------------------------------------------
- (BOOL)isEqualTo:(PFUser *)user
//-------------------------------------------------------------------------------------------------------------------------------------------------
{
    return [self.objectId isEqualToString:user.objectId];
}

@end
